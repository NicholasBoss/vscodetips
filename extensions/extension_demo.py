import math

# ! This is a demo of the math library
# * The math library is a built-in library in python
# * It provides functions for mathematical operations
# ? The math library provides functions like sin, cos, tan, sqrt, log, etc.

print(f'Sin: {math.sin(0)}')  # 0.0
print(f'Cos: {math.cos(0)}')  # 1.0
print(f'Tan: {math.tan(0)}')  # 0.0
print(f'Sqrt: {math.sqrt(4)}')  # 2.0
print(f'Log: {math.log(2)}')  # 0.6931471805599453

